#!/bin/bash

echo "Запускаем Editor Studio..."
echo "Браузер откроется автоматически!"

# Переходим в директорию скрипта
cd "$(dirname "$0")"

# Проверяем, установлены ли зависимости
if [ ! -d "node_modules" ]; then
    echo "Устанавливаем зависимости..."
    npm install
fi

# Запускаем сервер
# Мы используем nohup, чтобы отвязать процесс от терминала (если нужно запустить в фоне).
# Но так как Vite сам может завершаться, обычно удобнее оставить его в терминале.
# Для запуска в фоне и игнорирования вывода можно раскомментировать строку ниже:
# nohup npm run dev > /dev/null 2>&1 & echo "Сервер запущен в фоне (PID $!)."

# Обычный запуск (лучше, так как видно логи и можно остановить по Ctrl+C)
npm run dev
